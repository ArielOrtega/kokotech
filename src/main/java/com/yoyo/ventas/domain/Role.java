package com.yoyo.ventas.domain;

public class Role {
	
	private int rolId;
	private String typeName;
	
	public Role() {
		
	}
	
	public int getRolId() {
		return rolId;
	}
	public void setRolId(int rolId) {
		this.rolId = rolId;
	}
	public String getTypeName() {
		return typeName;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
}
